import os
import feedparser
import requests
from google import genai


# Get secrets from GitHub Actions
BOT_TOKEN = os.getenv("BOT_TOKEN")
CHANNEL_ID = os.getenv("CHANNEL_ID")
GEMINI_KEY = os.getenv("GEMINI_KEY")


RSS_FEEDS = [
    "https://feeds.marketwatch.com/marketwatch/topstories/",
    "https://www.coindesk.com/arc/outboundfeeds/rss/",
    "https://www.investing.com/rss/news.rss",
    "https://feeds.reuters.com/reuters/businessNews",
    "https://feeds.reuters.com/reuters/globalmarketsNews",
]


def get_news():
    articles = []

    for feed_url in RSS_FEEDS:
        try:
            feed = feedparser.parse(feed_url)

            if not hasattr(feed, "entries"):
                continue

            for entry in feed.entries[:3]:
                if hasattr(entry, "title") and hasattr(entry, "link"):
                    articles.append({
                        "title": entry.title,
                        "link": entry.link
                    })

        except Exception as e:
            print(f"RSS failed: {feed_url} -> {e}")

    return articles

def summarize_news(articles):

    headlines = "\n".join(
        f"- {article['title']}"
        for article in articles
    )

    prompt = f"""
You are a professional global macroeconomic news editor writing for a Telegram audience of students and finance enthusiasts.

Task:
- Read all provided headlines.
- Select the 5 most important global macroeconomic and financial stories of the day.
- Prioritize: inflation, GDP, central banks, interest rates, stock markets, oil/energy, crypto, major corporate events.
- Ignore low-impact, local, entertainment, and repetitive news.

Output format:

🌍 Global Economic News 💰📈

1. Story 🧠
Short explanation (2–4 sentences).

2. Story 🧠
Short explanation (2–4 sentences).

3. Story 🧠
Short explanation (2–4 sentences).

4. Story 🧠
Short explanation (2–4 sentences).

5. Story 🧠
Short explanation (2–4 sentences).

📌 Market Summary:
2–3 sentences about overall market mood.

Rules:
- Maximum 200 words.
- Do NOT invent data.
- Use numbers when available.

Headlines:
{headlines}
"""

    client = genai.Client(api_key=GEMINI_KEY)

    response = client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt
    )

    return response.text


def send_to_telegram(message):

    url = (
        f"https://api.telegram.org/"
        f"bot{BOT_TOKEN}/sendMessage"
    )

    payload = {
        "chat_id": CHANNEL_ID,
        "text": message
    }

    result = requests.post(
        url,
        json=payload
    )

    print(result.json())


def main():

    print("Getting news...")

    articles = get_news()

    print(f"Found {len(articles)} articles")

    summary = summarize_news(articles)

    print("Summary created")

    send_to_telegram(summary)

    print("Posted to Telegram")


if __name__ == "__main__":
    main()
